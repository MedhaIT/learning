package com.mit.selenium.learning.calculation;

public class Calculation {
	
    
    public int addition(int a,int b){
    	
    	return   a + b;
    }
 public int subraction (int c ,int d){
    	
    	return  c-d; 
    }
 public int multiplication (int e ,int f){
 	
 	return  e*f; 
 }
 public int divide (int g ,int h){
 	
 	return g/h; 
 }
 
}
